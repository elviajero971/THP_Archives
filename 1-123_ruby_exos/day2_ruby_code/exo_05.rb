#{} permet de faire une opération à l'intérieur string
puts "On va compter le nombre d'heures de travail à THP" #affiche ce message
puts "Travail : #{10 * 5 * 11}" #affiche Travail : puis multiplie les valeurs
puts "En minutes ça fait : #{10 * 5 * 11 * 60}" #affiche texte : puis multiplie les valeurs

puts "Et en secondes ?" #affiche ce message

puts 10 * 5 * 11 * 60 * 60 #multiple les valeurs

puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?" #affiche ce message

puts 3 + 2 < 5 - 7 #compare les chiffres et précise que 5 est inférieur à 12

puts "Ça fait combien 3 + 2 ? #{3 + 2}" #affiche texte : puis fait l'opération
